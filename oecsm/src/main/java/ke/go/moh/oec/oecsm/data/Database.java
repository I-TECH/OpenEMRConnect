package ke.go.moh.oec.oecsm.data;

import java.util.ArrayList;
import java.util.List;

/**
 * @date Aug 13, 2010
 *
 * @author Gitahi Ng'ang'a
 */
public class Database implements SchemaTransactionTarget, Comparable<Database> {

    private int id;
    private String name;
    private List<Table> tableList = new ArrayList<Table>();

    public Database() {
    }

    public Database(String name) {
        this.name = name;
    }

    public Database(int id, String name) {
        this.id = id;
        this.name = name;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public List<Table> getTableList() {
        return tableList;
    }

    public void setTableList(List<Table> tableList) {
        this.tableList = tableList;
    }

    @Override
    public boolean equals(Object obj) {
        if (obj == null) {
            return false;
        }
        if (getClass() != obj.getClass()) {
            return false;
        }
        final Database other = (Database) obj;
        if ((this.name == null) ? (other.name != null) : !this.name.equals(other.name)) {
            return false;
        }
        return true;
    }

    @Override
    public int hashCode() {
        int hash = 5;
        hash = 11 * hash + (this.name != null ? this.name.hashCode() : 0);
        return hash;
    }

    public int compareTo(Database that) {
        return this.name.compareToIgnoreCase(that.name);
    }

    @Override
    public String toString() {
        return this.name;
    }
}
